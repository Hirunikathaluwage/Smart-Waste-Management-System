package com.csse.smartwaste.common.model;

/**
 * WasteType Enum - Represents different types of waste
 */
public enum WasteType {
    BULKY_WASTE,    // Large items like furniture, appliances
    E_WASTE,        // Electronic waste
    ORGANIC,        // Food waste, garden waste
    RECYCLABLE,     // Paper, plastic, metal
    HAZARDOUS,      // Chemicals, batteries
    GENERAL         // Mixed general waste
}
